package w3resource;

public class Sumofprimenumbers {

	public static void main(String[] args) {
		int n=3; int temp=0; int sum=0;
		while(temp<=100) {
			
		for(int i =2;i<n;i++) {
			
		
		if( n%2!=0 && n%i!=0 && n%1==0 ) {
		
			sum=sum+n;
		
			

		}
		
		}	
		temp++; 
		
		n++;
	}
		
System.out.println(sum+2);
	}
	}
